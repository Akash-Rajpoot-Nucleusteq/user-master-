package in.nucleusteq.plasma.enums;

/**
 * Enumeration representing various visa statuses.
 */
public enum VisaStatus {
    /**
     * H1B visa status.
     */
    H1B,

    /**
     * F1 OPT visa status.
     */
    F1_OPT,

    /**
     * F1 STEM OPT visa status.
     */
    F1_STEM_OPT,

    /**
     * L1 visa status.
     */
    L1,

    /**
     * US Green Card visa status.
     */
    US_GREEN_CARD,

    /**
     * US Citizen visa status.
     */
    US_CITIZEN,

    /**
     * Canadian Permanent Resident visa status.
     */
    CANADIAN_PR,

    /**
     * Canadian Citizen visa status.
     */
    CANADIAN_CITIZEN,

    /**
     * L2 visa status.
     */
    L2,

    /**
     * J2 visa status.
     */
    J2
}
