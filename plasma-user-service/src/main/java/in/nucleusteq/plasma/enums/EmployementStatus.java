/**
 * Enumeration representing employment statuses.
 */
package in.nucleusteq.plasma.enums;

public enum EmployementStatus {
    /**
     * Indicates an active employment status.
     */
    ACTIVE,

    /**
     * Indicates an employment status where the employee is in a notice period.
     */
    NOTICE_PERIOD,

    /**
     * Indicates an employment status where the employee is on a sabbatical.
     */
    SABBATICAL,

    /**
     * Indicates a separated employment status where the employee is no longer with
     * the company.
     */
    SEPARATED
}
